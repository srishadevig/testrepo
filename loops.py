#For Loop

for letter in 'python':
    print("Current Letter: ",letter)
    
fruits=['apple','banana','orange']
for a in fruits:
    print("Fruit: ",a)
    
fruits=['apple','banana','orange']
for item in range(len(fruits)):
    print("Fruit: ",fruits[item])
    
